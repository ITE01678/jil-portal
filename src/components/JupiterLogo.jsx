/* ── Ray ring helper ────────────────────────────────────────────────
 * Renders one ring of sun rays as an SVG.
 * Wraps in a div for reliable CSS rotation (transform-origin: center).
 */
function RayRing({
  size,
  rayCount  = 16,
  innerFrac = 0.80,
  outerFrac = 1.00,
  color     = "#f59e0b",
  duration  = "22s",
  reverse   = false,
}) {
  const half   = size / 2;
  const innerR = half * innerFrac;
  const outerR = half * outerFrac;

  return (
    <div
      style={{
        position:        "absolute",
        inset:           0,
        width:           size,
        height:          size,
        transformOrigin: "50% 50%",
        animation:       `spin ${duration} linear infinite ${reverse ? "reverse" : "normal"}`,
      }}
    >
      <svg viewBox={`0 0 ${size} ${size}`} width={size} height={size}>
        {Array.from({ length: rayCount }).map((_, i) => {
          const angle  = (i * 360 / rayCount) * Math.PI / 180;
          const isLong = i % 2 === 0;
          const r1     = isLong ? innerR : innerR + (outerR - innerR) * 0.4;
          const x1 = half + r1       * Math.cos(angle);
          const y1 = half + r1       * Math.sin(angle);
          const x2 = half + outerR   * Math.cos(angle);
          const y2 = half + outerR   * Math.sin(angle);
          return (
            <line key={i}
              x1={x1} y1={y1} x2={x2} y2={y2}
              stroke={color}
              strokeWidth={isLong ? 2.2 : 1.2}
              strokeLinecap="round"
              opacity={isLong ? 0.88 : 0.45}
            />
          );
        })}
      </svg>
    </div>
  );
}

